namespace Korona\Http\Controllers;

use Illuminate\Http\Request;

use Auth;
use Cache;
use Korona\Http\Requests;
use Korona\Comment;

class CommentController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'commentable_type' => 'required|in:Korona\Post',
            'commentable_id'   => 'required|integer',
            'body'             => 'required',
        ]);

        $class = '\\'.$request->input('commentable_type');
        $target = $class::findOrFail($request->input('commentable_id'));
        $comment = new Comment();
        $comment->user_id = Auth::user()->id;
        $comment->body = $request->input('body');
        $target->comments()->save($comment);

        return redirect()->to($target->getUrl());
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $comment = Comment::findOrFail($id);

        return view('comment.edit', ['comment' => $comment]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'body'             => 'required',
        ]);

        $comment = Comment::findOrFail($id);
        $comment->body = $request->input('body');
        $comment->save();
        Cache::forget('comments.' . $id);

        return redirect()->to($comment->commentable->getUrl());
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $comment = Comment::findOrFail($id);

        $url = $comment->commentable->getUrl();
        $comment->delete();
        Cache::forget('comments.' . $id);

        return redirect()->to($url);
    }
}
